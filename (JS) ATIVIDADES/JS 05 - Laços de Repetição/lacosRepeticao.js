// Exercício 1: Mostrar no console os números de 1 ao 50. (while)
console.log("Exercício 1:");
let contador1 = 1;
while (contador1 <= 50) {
  console.log(contador1);
  contador1++;
}

// Exercício 2: Mostrar no console contagem regressiva de 10 até 0. (do-while)
console.log("Exercício 2:");
let contador2 = 10;
do {
  console.log(contador2);
  contador2--;
} while (contador2 >= 0);

// Exercício 3: Mostrar no console os números pares de 1 a 100. (for)
console.log("Exercício 3:");
for (let i = 2; i <= 100; i += 2) {
  console.log(i);
}

